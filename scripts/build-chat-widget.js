#!/usr/bin/env node
/**
 * chat-widget-inline.template.html + chat-i18n.json → chat-widget-inline.html を生成
 *
 * テンプレート内の __I18N_JSON__ を、chat-i18n.json から抽出した
 * widget専用の翻訳辞書で置換する。
 *
 * 辞書のキー名マッピング:
 *   widget側の短縮キー ← chat-i18n.json 側の namespaced キー
 * 新しいキーを追加する時は WIDGET_KEYS_MAP に一行足して
 * chat-i18n.json 側にも対応する値を入れること。
 */
const fs = require('fs');
const path = require('path');

const ROOT = path.resolve(__dirname, '..');
const TEMPLATE = path.join(ROOT, 'chat-widget-inline.template.html');
const OUT = path.join(ROOT, 'chat-widget-inline.html');
const OUT_BODY_JS = path.join(ROOT, 'chat-widget-body.js');
const I18N_JSON = path.join(ROOT, 'chat-i18n.json');

// widget側キー → chat-i18n.json側キー
const WIDGET_KEYS_MAP = {
  empty: 'widget.empty',
  note: 'visitor.note',
  phMsg: 'widget.phMsg',
  phNick: 'widget.phNick',
  send: 'input.send',
  online: 'status.online',
  offline: 'status.offline',
  checking: 'widget.checking',
  unavailable: 'widget.unavailable',
  connErr: 'widget.connErr',
  min2: 'widget.min2',
  max500: 'widget.max500',
  sendErr: 'widget.sendErr',
  commErr: 'widget.commErr',
  sessErr: 'widget.sessErr',
  unavailableName: 'widget.unavailableName',
  fsS: 'widget.fsS',
  fsM: 'widget.fsM',
  fsL: 'widget.fsL',
  noteReservation: 'note.reservation',
  receptionClosed: 'reception.closed',
  receptionHours: 'reception.hours',
  receptionSendOk: 'reception.sendOk',
  receptionNextOpen: 'reception.nextOpen',
};

function main() {
  const i18n = JSON.parse(fs.readFileSync(I18N_JSON, 'utf8'));
  const template = fs.readFileSync(TEMPLATE, 'utf8');

  const widgetI18N = {};
  const missing = [];
  for (const lang of Object.keys(i18n)) {
    widgetI18N[lang] = {};
    for (const [wKey, srcKey] of Object.entries(WIDGET_KEYS_MAP)) {
      const v = i18n[lang][srcKey];
      if (v == null) {
        missing.push(`${lang}:${srcKey} (needed for widget.${wKey})`);
        widgetI18N[lang][wKey] = i18n.ja[srcKey] || wKey;
      } else {
        widgetI18N[lang][wKey] = v;
      }
    }
  }
  if (missing.length) {
    console.error('ERROR: chat-i18n.json に不足キーあり:');
    missing.forEach(m => console.error('  -', m));
    process.exit(1);
  }

  // JSON.stringify でインジェクト（特殊文字は安全にエスケープされる）
  const injected = JSON.stringify(widgetI18N);
  if (!template.includes('__I18N_JSON__')) {
    console.error('ERROR: chat-widget-inline.template.html に __I18N_JSON__ プレースホルダーが見つかりません');
    process.exit(1);
  }
  const output = template.replace('__I18N_JSON__', injected);

  fs.writeFileSync(OUT, output);
  console.log(`✓ Generated ${path.basename(OUT)} (${output.length} bytes, ${Object.keys(i18n).length} langs × ${Object.keys(WIDGET_KEYS_MAP).length} keys)`);

  // ---- chat-widget-body.js を派生生成（動的ローダー経由の埋込用） ----
  const bodyJs = buildBodyJs(output);
  fs.writeFileSync(OUT_BODY_JS, bodyJs);
  console.log(`✓ Generated ${path.basename(OUT_BODY_JS)} (${bodyJs.length} bytes)`);
}

/**
 * chat-widget-inline.html から chat-widget-body.js を生成。
 *
 * 変換:
 *   <style>CSS</style>        → 動的 <style> 注入
 *   <div class="ychat-wrap" id="ychat-root">…</div>
 *                              → document.getElementById('ychat-root') に innerHTML 注入
 *   <script>(function(){…})()</script>
 *                              → そのまま IIFE として実行（内部で document.getElementById 参照するため
 *                                 HTML注入後に実行する必要あり）
 *
 * 貼付コード側:
 *   <div id="ychat-root" data-slug="xxx"></div>
 *   <script>…ローダー…</script>
 */
function buildBodyJs(html) {
  // style ブロック抽出
  const styleMatch = html.match(/<style[^>]*>([\s\S]*?)<\/style>/);
  if (!styleMatch) throw new Error('chat-widget-inline.html に <style> が見つかりません');
  const css = styleMatch[1];

  // .ychat-wrap div の内部HTMLを抽出（開始タグ属性は廃棄、内部だけ使う）
  const wrapMatch = html.match(/<div class="ychat-wrap"[^>]*>([\s\S]*?)<\/div>\s*<script/);
  if (!wrapMatch) throw new Error('chat-widget-inline.html に .ychat-wrap div が見つかりません');
  const innerHtml = wrapMatch[1].trim();

  // script ブロック抽出（IIFE 本体）
  const scriptMatch = html.match(/<script[^>]*>([\s\S]*?)<\/script>/);
  if (!scriptMatch) throw new Error('chat-widget-inline.html に <script> が見つかりません');
  const scriptBody = scriptMatch[1];

  // ローダー経由で data-slug が動的に差し替わるので、テンプレート側のハードコード値はそのまま残す
  // （呼び出し側が上書きするため）
  return `/*! YobuChat widget body — auto-generated by scripts/build-chat-widget.js. Do NOT edit directly. */
(function(){
  if (window.__ychatBodyLoaded) return;
  window.__ychatBodyLoaded = true;
  var slot = document.getElementById('ychat-root');
  if (!slot) { if (window.console) console.warn('[YobuChat] #ychat-root が見つかりません'); return; }
  // CSS 注入
  var __yc_css = ${JSON.stringify(css)};
  var __yc_style = document.createElement('style');
  __yc_style.type = 'text/css';
  __yc_style.textContent = __yc_css;
  document.head.appendChild(__yc_style);
  // HTML 注入: slot を .ychat-wrap 化して内部を埋める
  slot.className = (slot.className ? slot.className + ' ' : '') + 'ychat-wrap';
  slot.innerHTML = ${JSON.stringify(innerHtml)};
  // 既存 IIFE をそのまま実行（内部で document.getElementById('ychat-root') 他を参照）
  ${scriptBody}
})();
`;
}

main();
